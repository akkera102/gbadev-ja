//---------------------------------------------------------------------------
s32 CodeRom(void)
{
	while(*(vu16*)0x4000006 >= 160) {};
	while(*(vu16*)0x4000006 <  160) {};

	s32 ret = 0;

	while(*(vu16*)0x4000006 >= 160)
	{
		ret++;
	}

	return ret;
}
